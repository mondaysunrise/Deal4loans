        <br>
        LIC has tied-up with Karnataka-based Corporation Bank for its white-labeled credit cards. The bank will be the card issuer. <br>
        The launch was deferred due to "some technical reasons," the LIC spokesperson said. <br>
        <br>
        LIC had asked Corporation Bank to sort out the problems before the launch itself so that there were no issues later on, the spokesperson said. <br>
        <br>
        When contacted, a Corporation Bank official said that the bank had sorted out all technical problems and was now ready for the launch. <br>
        <br>
        "The launch was delayed since the card is a white- label credit card which requires some formalities (to be completed). But now we are ready and awaiting the launch date," Corporation Bank's General Manager (New Initiatives), B R Bhat, said. <br>
