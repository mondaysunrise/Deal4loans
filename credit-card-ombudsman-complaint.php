        </p>
        <p>The increase was also observed in the year 2006-07 with a 22 per cent increase from the year 2005-06, said the Reserve Bank of India's annual report on banking ombudsman. </p>
        <p>In 2005-06, the ombudsman offices received 31,732 complaints and in 2006-07, the number of complaints received was 38,638.</p>
        <p>The increase in the number of complaints received in 2005-06 and 2006-07 can be attributed to the inclusion of new areas - such as credit card complaints and also allowing complaint submission in any form, such as online and by email - in the Banking Ombudsman Scheme, 2006, the RBI report said. </p>
        <p>The maximum number of complaints dealt with during the last five-year period included complaints regarding deposit accounts, deficiency in servicing of loans and advances and delay in collection of cheques/bills. </p>
        <p> However, in 2006-07, the largest share of complaints received were credit cards complaints, which accounted for 20 per cent. Among the complaints received, credit cards-related complaints was 7,688, deposit accounts-5,803, remittances-4,058 and loans and advances (general)-4,442.</p>
        <p> Some of the types of credit card complaints received were non-issue of credit cards, issue of unsolicited cards, non-despatch of account statements in time, levy of excessive service charges, levy of late fees despite payments being done on time, refusal to cancel credit card, freezing of cards without informing the cardholder and harassment by recovery officer. <br>
