        <p>Credit card is an unsecured loan, means that unlike a secured loan, which is advanced by a bank/financial institution against a security like property for instance, a credit card is offered without any security. Hence banks take necessary steps to ensure that only those meeting certain parameters are qualified to use their credit card. </p>
        <p>One of the major things in a credit card statement is <B>Minimum Amount Due (MAD), which is 5% of the Total Amount Due.</B> This is the only detail you will find relatively well highlighted in  your monthly account statement. This states that you must pay for the  purchases done in that month so as to not attract a penalty for default  on payment of card dues. But there are charges levied then also if you  pay you Minimum Amount Due before the due date. Lets have a look on the  IV possible scenarios</p>
        <p><B>(I) If you pay don&rsquo;t pay anything till the due date, then you are liable to pay the following charges:</B><br>
          <br>
          &bull; Late Payment Charges<br>
          &bull; Default Charges<br>
          &bull; Processing Fee<br>
          &bull; Transaction fees<br>
          &bull; Service Tax<br>
          &bull; APR</p>
        <p><B>(II) If you pay less than the Minimum Amount Due before the due date then also you need to pay the above-mentioned charges.</B></p>
        <p><B>(III) If you pay more than or equal to Minimum Amount due but  less than the total amount due then you will be categorized as a  Revolver. And then you will be levied the following charges:</B></p>
        <p><B> &bull;    APR</B> from the date of the transaction and on the full amount of the transaction<B><br>
          &bull;    Processing Fee </B>on the full amount of the transaction<B><br>
          &bull;    Service Tax </B><br>
          Till the time you don&rsquo;t pay the total amount due on your card with all  the charges levied you cannot get out of your credit card debt.</p>
        <p><B>(IV) If you pay The Total Amount due and that too before  the due date then you can always take the maximum benefit of your  credit card!</B></p>
        <p><B>It is recommend that you should pay the entire sum,  i.e. Total Amount Due before the due date on your card. Buying on a  credit card is okay till the time you pay your bills religiously. The  moment you carry forward your payment to the next monthly cycle, you  will have to pay interest on the unpaid amount along with taxes. Once  you understand these scenarios, you will be able to spend and use your  credit card judiciously.</B> <br>
