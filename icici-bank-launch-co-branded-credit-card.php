Private sector lender ICICI Bank in association with south-east Asian air-carrier Singapore Airlines Launches the co-branded <br><br>

'ICICI Bank Singapore Airlines Visa Platinum Card' in the Indian market. <br><br>

"This is the first time that we (Singapore Airlines) have launched a credit card in India. The synergy that is created by this partnership is unprecedented," Singapore Airlines' Regional Vice-President (West Asia and Africa) Lee Lik Hsin said at the launch function. <br><br>

Singapore Airlines is confident that the card would bring unparalleled benefits to its loyal customers and further boost the appeal of Kris Flyer programmed in India, Hsin said. <br><br>

Kris Flyer is Singapore Airlines frequent-flyer programmed under which its enrolled members can redeem their miles for free flights on over 20 global airlines to more than 820 destinations across 160 countries. <br><br>

ICICI Bank has issued 7.5 million credit cards in India of which around 10 per cent are in the premium segment. Singapore Kris Flyer programmed claims to have 80,000 members enrolled in India.<br><br>

