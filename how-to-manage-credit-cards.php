<div class="ecc-head-text">Multiple Credit Cards</div>
<div style="clear:both;"></div>
<div class="banner-ecc"><img src="images/multiple-credit-card.jpg" style="max-width:920px" width="100%"  alt="multiple credit card" /></div>
<div style="clear:both;"></div>
<div class="text-wrapper-ecc">Once you have one Card you are flooded with offers on other Bank Credit cards and at time you end up taking these 4-5 Cards different bank cards.<br />
  Most cards come with some sort of fees and then you feel the pinch.<br /><br />
<strong style="font-size:18px;">Remedy</strong>
<br />
 Opt for Max 2 Credit Cards of that  one can be Visa and other can be master card .<br />
Opt for Free Annual charge Credit Cards and one card should be Co-Branded as per your usage needs.<br />
