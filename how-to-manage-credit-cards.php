<div class="box-evil-1">
<div class="numbering-text">1
<div style="clear:both;"></div>
</div>
<div class="ecc-head-text">Rate of Interest</div>
<div style="clear:both;"></div>
<div class="banner-ecc"><img src="images/rate-of-interest-image.jpg" style="max-width:920px" width="100%"  alt="Rate of Interest" /></div>
<div style="clear:both;"></div>
<div class="text-wrapper-ecc">Most Card companies charge 2.5% - 3.9%  pm of interest if you don’t pay 100% of the dues.<br />
All Card companies quote the interest rates as monthly but when you look at it is 30%-50% yearly.<br />
So if you have a 1 lac due on your card you will have to pay Rs 2500- 4100 monthly as interest and which is as high as Rs 30000-50000 yearly.<br /><br />
<strong style="font-size:18px;">Remedy</strong>
<br />
Only use Credit Card as a convenience of not to carry cash and pay 100% on due date and only use 30-45 day of free credit period given by Bank.
Never use Credit Card when you are not sure that you can pay 100% of your dues. </div>
</div>
<div style="clear:both;"></div>
<div class="box-evil-1">
<div class="numbering-text">2
  <div style="clear:both;"></div>
</div>
<div class="ecc-head-text">Withdrawal of Cash from Credit Card</div>
<div style="clear:both;"></div>
<div class="banner-ecc"><img src="images/cash-from-credit-card.jpg" style="max-width:920px" width="100%"  /></div>
<div style="clear:both;"></div>
<div class="text-wrapper-ecc">Most Banks give you access to withdraw Cash on your  Credit Cards from ATMs.<br />
  Consumers thinks it is at a nominal cost but it come with interest rate of 35-50% annually.
  <br /><br />
<strong style="font-size:18px;">Remedy</strong>
<br />
Credit Card is not an ATM card – only use Debit Cards to withdraw cash.<br />
Even if you withdraw money in emergency , ensure you pay as early as possible.<br />
</div>
</div>
<div style="clear:both;"></div>
<div class="box-evil-1">
<div class="numbering-text">3
  <div style="clear:both;"></div>
</div>
<div class="ecc-head-text">Minimum payment Due</div>
<div style="clear:both;"></div>
<div class="banner-ecc"><img src="images/payment-summry.jpg" style="max-width:920px" width="100%"  alt="Payment Summary" /></div>
<div style="clear:both;"></div>
<div class="text-wrapper-ecc">When you receive statements you see a minimum payment due amount which is 5% of your total bill.<br />
  A large part of Consumers think that is payable at that time and pay that much amount only.Instead Pay 100% of amount due, <br />
  as on rest 95% you have to pay 36%-50% rate of interest.<br /><br />
<strong style="font-size:18px;">Remedy</strong>
<br />
Only look at total due on your Credit Cards. Say yes to 100% payments on Credit Cards.</div>
</div>
<div style="clear:both;"></div>
<div class="box-evil-1">
<div class="numbering-text">4
  <div style="clear:both;"></div>
</div>
<div class="ecc-head-text">How to get real Reward points.</div>
<div style="clear:both;"></div>
<div class="banner-ecc"><img src="images/reward-points.jpg" style="max-width:920px" width="100%"  alt="Reward Points" /></div>
<div style="clear:both;"></div>
<div class="text-wrapper-ecc">All Banks offer usage based reward points or Cash backs on shopping on Brands and Online  shopping discounts these days.<br />
  A consumer needs to understand what he needs to use his Credit Card for.Otherwise all what looks like a attractive offer on shopping from credit cards ,doesn’t actually get you any discounts<br /><br />
<strong style="font-size:18px;">Remedy</strong>
<br />
<p>Where do you spend most i.e. Petrol/ Airtickets/Shopping @ Particular store.<br />
  If at any place you spend more than 5000 a month and that place has a Cobranded card – just get that card- it will give you actual rewards.<br />
  You can save 1-2% of your monthly spends via that route and ensure you save money by using credit Cards and earn from it.</p>
<p></p>
</div>
