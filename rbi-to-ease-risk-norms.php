        <p> State-owned banks are planning to cut rates on small-ticket <a href="http://www.deal4loans.com/home-loans.php">home loans</a> by up to 300 basis points by Friday, as RBI separately considers ways to help the banking sector make small home loans available at cheaper rates and relaxed norms, bankers said. </p>
        <p>"We have decided to bring down interest rates for <a href="http://www.deal4loans.com/">loans</a> up to Rs 20 lakh by 200 basis points while loans up to Rs 5 lakh would be cheaper by 300 basis points," UCO Bank chairman and managing director SK Goyal told ET. </p>
        <p>Oriental Bank of Commerce (OBC) and Bank of India are also considering rate cuts by the end of this week, officials told ET, although the extent could vary significantly. The planned cuts will bring down rates for home loans up to Rs 20 lakh to between 8% and 10%. Nearly 80% home loans are below this amount.</p>
        <p>Meanwhile, RBI is likely to lower risk weight ages for home loans of up to Rs 20 lakh, according to government officials and bankers with knowledge of the central bank's thinking. Banks have to assign credit risks to all their assets, which vary between 0% and 100%, depending on the asset's risk. Based on the risks, they have to maintain minimum capital. A lower risk weight age will allow banks to give more loans without raising more capital. </p>
        <p>A finance ministry official, who asked not to be named, said RBI could also ease provisioning norms in case of small housing loans <br>
          <br>
          Banks have to keep certain money aside - known as capital adequacy ratio, or CAR, in banking parlance - for every loan. Currently, the CAR is set at 9%, which means for every loan of Rs 100 that carries 100% risk, banks have to keep aside Rs 9 as capital to cover the risk.</p>
        "In case of <a href="http://www.deal4loans.com/home-loans.php">housing loans</a>, at present, RBI prescribes a risk of 75%. We expect that the central bank may reduce this to 50% for loans up to Rs 20 lakh," said OBC executive director SC Sinha. This will mean that banks will need to set aside a smaller amount of capital to protect against defaults. Several banks are also likely to reduce the amount of margin money that customers need to put up for loans up to Rs 20 lakh, government officials said.<br />
