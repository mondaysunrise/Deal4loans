  <div id="txt"><h1>'Don't expect cheap loans till RBI cuts repo rate, SLR'</h1>
   
<b>MUMBAI:</b> Bank loans will become cheaper only if the Reserve Bank of India (RBI) signals a softer interest regime through a repo rate cut or if statutory liquidity requirements are reduced, according to a Crisil study for Ficci and IBA. </p><p>

High borrowing costs on account of a continued scramble for deposits are expected to keep lending rates steady. However, banks and borrowers could get a breather if RBI slashes its repo rates or reduces the SLR investment limit (the portion of bank deposits that must be invested in government bonds) which could provide a much-needed breather to banks. </p><p>

Until last year, banks sold surplus bonds to generate funds for lending. However, in recent months, most banks are very close to the minimum SLR level, limiting the scope for further sale of bonds. This has increased their dependence on deposits for future credit growth. Deposits continue to be a mainstay of banks' resource mix. As PF March 31, 2007, deposits constituted over 77% of the total liabilities of all commercial banks compared with 81%, a year ago. </p><p>

Though banks are broad-basing their core-income streams by diversifying into fee-based activities, their contribution to banks' profitability would substantiate only in the long-run, their core profitability levels, according to the study. </p><p>

Banks credit growth, though reined somewhat in recent months, is expected to remain high at around 25% in 2007-08. The proportion of bulk deposits will continue to grow, but at a slower pace. Crisil expects banks to gradually shift their focus towards retail term deposits. </p><p>

<b>Current and savings accounts' (CASA)</b>  a source of low-cost funds  levels are likely to reduce marginally across the banking system over the medium to long term, with increasingly sophisticated depositors opting for higher-yielding investments. The systemic cost of deposits will be driven by structure of the resource mix, and not solely by the interest rate environment. Thus, the war for resources is expected to continue in the medium term. </p><p>

According to Crisil, final Basle-II guidelines will bring about a sharp change in capital adequacy levels in the banking sector as far as credit risk is concerned. With respect to corporate loan books, PSBs are set to gain more than private banks. This gain will accrue if banks get their loan portfolio rated. <br>
