      <!--class="lfttxtbar" -->
      <div>
        <p>Whirlpool of India is entering the arena of financial services by partnering with Videocons Next Retail for giving easy and flexible credit to consumers. Forming a tripartite arrangement with <a href="http://www.deal4loans.com/loans/banks/icici-bank-icici/">ICICI Bank</a>, the manufacturer and retailer are joining hands to bear the interest cost burden of the loan given by a bank.</p>
        <p>Speaking to Business Line, Mr Sukhpreet Singh, General Manager, Brand Marketing, Whirlpool of India, said, The consumer would benefit as we would be bearing the interest cost for providing the <a href="http://www.deal4loans.com/">loan</a> along with retailer. With this arrangement we would expect to garner a 10-per cent counter share across the Next outlets as the retailer is a key account for us.</p>
        <p>Currently, Next Retail has 334 outlets and its exclusive tie-up with Whirlpool of India would ensure additional share for the brand at its outlets.</p>
        <p>The fact that a home appliance manufacturer is taking on the interest burden of the consumer is a new concept in the market and with this we would be providing a better value proposition to our customers. With this arrangement consumers do not get to pay that much more as the cost is now borne by different partners, said Mr Singh. With the purpose of bringing down the interest burden further, Whirpool is trying out this arrangement with Next Retail, before extending its services to other retailers.</p>
        <p>To control prices</p>
        <p>Meanwhile, Whirlpool is planning to keep its prices under control in spite of the rise in input costs. We have brought down our logistics and distribution costs and are using our global sourcing strengths to drive cost efficiencies. Besides we are not selling on credit and have adopted a cash and carry business model, said Mr Singh.</p>
        <p>Whirpool claims that its price hikes have been marginal at 2 per cent across selected products. Admitting to input cost pressures Mr Singh said, Input costs of items such as steel, zinc and petroleum derivatives have risen but we have managed to grow on a quarter to quarter basis.</p>
        <p>Having entered the electronic water purifier category recently, Whirlpool is looking at garnering a 10 per cent share in this category.</p>
        With Kajol and Ajay Devgan as its brand ambassadors, Whirlpool has been targeting the woman and her family as its target audience. We always place the woman at the centre while designing our products, states Mr Singh. This year Whirlpool has earmarked Rs 77 crore towards advertising and marketing spends. <br />
