      <div>
        <p>Getting bank loans has become tougher. Because banks are closely watching your  credit history. This applies even if you have defaulted in paying a paltry sum  on a loan taken several years ago. </p>
        <p> Banks today have logged on to data supplied by the Credit Information Bureau of  India (CIBIL). CIBIL provides information on the credit history of commercial  and individual borrowers to its members that include all banks, financial  institutions, nonbanking financial companies and housing finance companies. </p>
        <p> &ldquo;Even if a person had defaulted on a payment of Rs 500 several years ago, we  will take that into consideration. Recently we rejected a loan application </p>
        <p>because the person defaulted on an amount of Rs 28,000 in  2006. We checked his record from CIBIL,&rdquo; says a HDFC Bank official.</p>
        <p> Just a year ago, banks would have hounded you to take loans  irrespective of your &lsquo;track record&rsquo;. But that&rsquo;s now history. And it&rsquo;s causing  heartburn among consumers. Leading auto dealers like Magnum Honda and Cauvery  Ford have reported a 15%-20 % decline in new loan disbursals to potential  customers of mid-size cars on account of CIBIL data. That rejection figure  rises to 25%-30 % in the purchase of small cars. </p>
        <p> Even home loans are being rejected on account of this. Says a real estate  developer, &ldquo;Though there are genuine home buyers, what is affecting the market  now is the fact that they are not getting loans.&rdquo; </p>
        <p> CIBIL collects data from all members and puts borrowers into two broad  categories: a positive and a negative list. If you start defaulting on payments  or have outstanding dues even to credit  card companies, your name moves from the positive list to the  negative list. And it is this negative list that banks refer to when issuing  fresh loans now. </p>
        <p> &ldquo;We don&rsquo;t lend to people if their names appear in the negative list of CIBIL,  unless they come with a clean chit or a no-dues certificate from the bank,&rdquo;  says B R Pai, GM (retail banking) of Syndicate Bank. </p>
        <p> Incidentally, names could appear in the negative list even after the borrower  has cleared the entire amount because CIBIL updates the list only on a  quarterly basis.<br>
