      <b>When can I apply for a Home Loan?</b>
      <p> You can for apply for <a href="home-loans.php">Home Loan</a> at anytime.
        You may apply for it after you have decided to acquire/construct a property, and even in case, the property has not been selected or the construction has not commenced, you can still apply. What's more, you can also avail for Home Loan facility if you want to renovate or expand your home. </p>
      <br />
      <b>How do I make an application?</b>
      <p>You need to submit the application form along with the necessary documents.
        The bank, after going through your application will review it, ask questions wherever necessary and will also informally tell you the loan amount you are eligible for and the terms and conditions of the same and put across its decision to you. 
        You are as always, advised to shop around for more than one bank so as to get better terms/larger loan amount/lowest interest etc.</p>
      <br />
      <b>How is my Home Loan eligibility determined?</b>
      <p> Your <a href="http://www.deal4loans.com/home-loans.php">Home Loan</a> eligibility  is determined by your repayment capacity, taking into consideration, factors such as: 
        Your:<br>
        &bull; Income<br>
        &bull; Qualifications<br>
        &bull; Age<br>
        &bull; Spouse's income<br>
        &bull; No. of dependants<br>
        &bull; Stability and continuity of occupation<br>
        &bull; Assets/LiabilitiesM.<br>
        &bull; Savings history.<br>
        The most important concern of banks in determining your loan eligibility is that whether or not you are contentedly able to pay off the amount you borrow.</p>
      <br />
      <b>What are the types of home loans available?</b>
      <p> These are a range of Home Loans available:<br>
        &bull; Home Purchase Loans: for the purchase of a new home. <br>
        &bull; Home Improvement Loans: for implementing repair works/renovations in a home that you have already purchased. <br>
        &bull; Home Construction Loans : for the construction of a new home. <br>
        &bull; Home Extension Loans: for expanding/extending an existing home.<br>
        &bull; Land Purchase Loans: for purchase of land for home construction/investment purposes.<br>
        &bull; Home Conversion Loans: for those who have financed the present home with a home loan and wishes to purchase/move to another home for which some extra finances are required. 
        In Home Conversion Loan, the existing loan is transferred to the new home including the extra amount required, eliminating the need for pre-payment of the previous loan.<br>
        &bull; Balance Transfer Loans: helps you to pay-off an existing home loan and avail the option of a loan with a lower rate of interest.<br>
        &bull; Bridge Loans: for people who wish to sell the existing home and purchase another. This loan helps you finance the new home, until you find a buyer for your old home.<br>
        &bull; Refinance Loans: helps you pay off the debt you have incurred from private sources like relatives/friends, for the purchase of your present home. <br>
        &bull; Stamp Duty Loans: is sanctioned to pay the stamp duty amount that needs to be paid on the purchase of property. <br>
        &bull; Loans to NRIs: to build/buy a home in India.
        EMI is payable till the loan is paid back in full. It consists of a portion of the interest as well as the principal. <br>
        <br>
        <b>Some of the incentives offered by banks are :</b><br>
        &bull; Loan sanction without requiring you to identify property as a pre-requisite for eligibility.<br>
        &bull; Free accident/property insurance.<br>
        &bull; Waiving of pre-payment penalty/processing fee. </p>
      <br />
      <b>How long does it take to get my application processed and the loan sanctioned?</b>
      <p> It takes around two weeks for processing of one's application if all the necessary documents are in order and takes another week for the bank to inspect the property papers and make the disbursement.</p>
      <br />
      <b>What is the maximum amount I can borrow?</b>
      <p> The maximum amount that you can borrow depends on factors such as:<br>
        &bull; The purpose of the loan.<br>
        &bull; Whether it is for purchase of property or improvement or renovation.<br>
        &bull; Or purchase of land for development etc.<br>
        Besides, your residential status (whether resident Indian or Non-Resident Indian) will also be significant on the maximum quantum of loan that you can borrow. Typically <a href="http://www.deal4loans.com/home-loans.php">Home Loans</a> are provided for in the range of 75%-85% of the cost of the property, including cost of land.</p>
      <br />
      <b>What security/collateral do I have to provide?</b>
      <p> Banks usually take some additional securities which are called collateral securities. Collateral could be in the form of guarantee from one or two persons, assignment of <a href="http://www.bimadeals.com/life-insurance-india/life-insurance.php" target="_blank">life insurance policies</a>, the surrender value of which should be equal to the loan amount, deposit of shares, and units or other securities. 
        These additional securities are taken just incase a loan is not paid back; recourse may be taken to such securities instead of depending upon the mortgage of the property which is the last resort . </p>
      <br />
      <b>Does the property have to be insured?</b>
      <p> Yes, and you will have to insure that the property for fire and other appropriate hazards, as required by the banks during the loan tenure. The banks will be the beneficiary of the <a href="http://www.bimadeals.com/" target="_blank">insurance policy</a> . You will also have to produce proof/evidence, whenever required by the banks. This is an added cost that will add to the final cost of purchase of the property. </p>
      <br />
      <b>Can I take a Home Loan for construction in one city while working in another city?</b>
      <p> Yes, you can take loan for construction in one city while working in another. The banks usually service this loan after getting details of the plot legally verified.</p>
      <br />
      <b>What is the range of interest rates offered?</b>
      <p>The <a href="http://www.deal4loans.com/home-loans-interest-rates.php">home loan interest rate</a> varies from banks to banks and normally ranges from 12.5% to 16%.</p>
      <br />
      <b>How is the interest rates calculated on my loan?</b>
      <p> Most banks follow the yearly reducing-balance method, which accounts for your principal repayments only at the end of their financial year. As a result, you pay interest on the principal that you have already returned to the bank. 
        The effective interest rate is therefore higher than the quoted interest rate by around 0.7%. Some banks may also follow the daily or monthly reducing-balance method, which results in a lower interest burden.</p>
      <br />
      <b>What are the bases of interest rates calculation?</b>
      <p> The interest on Home Loans is usually calculated on Monthly Reducing or Yearly Reducing balance. 
        In Monthly Reducing Balance, the principal on which you pay interest reduces every month as you pay your EMI. 
        However in Yearly Reducing Balance, the principal is reduced at the end of the year, therefore you continue to pay interest on a certain part of the principal which you have actually paid back to the bank, which basically means the EMI for the Monthly Reducing system is effectively lesser than the Yearly Reducing system of calculating the interest. </p>
      <br />
      <b>What is the Fixed Rate of Interest?</b>
      <p> Fixed Rate of Interest means that the interest rates remain FIXED for the entire duration the loan. This basically means that you do not benefit, even if the rates of interest drop in the market.</p>
      <br />
      <b>And Floating Rate of Interest?</b>
      <p> This is the rate of interest that fluctuates according to the market lending rate. </p>
      <br />
      <b>What are the repayment period options?</b>
      <p> The maximum period over which one can pay the loan varies for every bank, and is also different for every scheme. Also your residential status makes a difference. If you are a resident Indian, you could avail of a loan for duration of 5-20 years.
        Few banks offer a 20-year repayment period, generally at a higher interest rate. As a Non-Resident Indian, you can only avail of a loan for a maximum period of 7 years. </p>
      <br />
      <b>Can I repay my loan ahead of schedule?</b>
      <p> Yes, you can pay your loan ahead of schedule, if you want to.
        However, it must be noted that banks charge a fee for early redemption of loan. This fee can vary between 1-2% of the loan amount being pre-paid. </p>
      <br />
      <b>Who can be a Co-Applicant?</b>
      <p> A Co-Applicant(s) is/are the Co-Owners of the property in respect of whom the financial assistance has been sought. 
        However all co-applicants need not be co-owners.
        Usually co-applicants are: husband/wife, father/son, mother/daughter etc. </p>
      <br />
      <b>What are the fees and charges payable and when are they payable?</b>
      <p> Banks charge fees at the time of application (processing fee) and at the time of loan sanction (administrative fees). 
        The processing fee is either a fixed amount not linked to the loan or it may be a percentage of the loan amount. The loan amount received by you can be less than the processing fee. 
        And as for the administrative fee, 1% of the loan amount sanctioned will have to be paid. 
        Both the processing fees and administration fees are payable upfront.<br />
        <br />
        Home Loans may also be accompanied by the following extra costs:<br>
        &bull; Interest Tax: is the tax payable on the interest paid on a home loan and not the principal. This tax is some times included in the interest rate of the loan, or may be charged separately as interest tax.<br>
        &bull; Commitment Fees: Some banks levy a commitment fee in case the loan is not availed of within a stipulated period of time after it is processed and sanctioned.<br>
        &bull; Miscellaneous costs: It is somewhat possible that some banks may levy a documentation or consultant charges </p>
      <br />
      <b>What is the EMI?</b>
      <p> EMI or Equated Monthly Installments refers to the fixed sum of money that you will be paying to the bank every month. 
        The EMI comprise of both interest and principal repayment. The amount of the EMI depends on the quantum of loan, interest rate applicable and the term of the loan. </p>
      <br />
      <b>In how many installments can the loan be disbursed?</b>
      <p> The loan can be disbursed in full or in suitable installments (normally not exceeding 3 months) taking into account the requirement of funds and progress of construction, as assessed by the bank. </p>
      <br />
      <b>Do I get a tax benefit on Home Loan?</b>
      <p> Yes, you are eligible for tax benefits on the principal and interest components of the loan under the Income Tax Act, 1961. 
        However as the benefits could vary each year, do check out the current benefits available. </p>
      <br />
      <b>Does the agreement for sale have to be registered?</b>
      <p> Yes, and you are advised to do so too as the Agreement for Sale between the builder and the purchaser is required by law to be registered. You can register at the office of the Sub-registrar appointed by the State Government under the Indian Registration Act, 1908. </p>
      <br />
      <b>What are the documents required at the time of application?</b>
      <p> Each bank has its own list of documents that one must submit at the time of application.
        The common documents that the banks require at the pre-approval stage are:<br>
        &bull; Passport size photograph. <br>
        &bull; Proof of Age. <br>
        &bull; Copy of Bank A/C statements for the last 6 months. <br>
        &bull; Copy of latest <a href="credit-cards.php">credit card</a> statement. <br>
        &bull; Signature verification from your banker.<br>
        <br>
        <b>If you are self-employed you require :</b><br>
        &bull; Your business track record .<br>
        &bull; Copy of audited financial statements for the last 2 years. <br>
        <br>
        <b>If you are salaried, you need :</b><br>
        &bull; Salary and TDS certificate <br>
        &bull; Latest pay slip.<br>
        &bull; Letter from employer.<br>
        <br>
        <b>And at the disbursal stage (for property already located), you need to submit the following :</b><br>
        &bull; Allotment letters. <br>
        &bull; Photocopies of title deeds.<br>
        &bull; Encumbrance certificate.<br>
        &bull; Agreement to sell .<br>
        <br>
        <b>For self-construction:</b><br>
        &bull; Approved plans and clearance certificates along with estimates. </p>
