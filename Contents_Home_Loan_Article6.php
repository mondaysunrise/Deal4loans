      <div id="txt" class="tbl_txt"> <font face="Verdana" color="0F74D4" size="2"><b>Housing finance cos fare well; analysts see bright future ahead</b></font> </font>
        <p> Our Bureau <br>
          Mumbai, Sept. 7 Housing Development Finance Corporation was at an all-time high of Rs 2,140 on Friday on the BSE. Despite the sub-prime originating out of housing finance companies in the global markets, domestic housing finance companies such as HDFC and LIC Housing Finance are faring well.</p>
        <p> Analysts feel that the banking finance sector is comparatively better off than others, one of the reasons being the low mortgage penetration to GDP ratio i.e., the amount of mortgage banks have in turn for their lending for housing to the GDP. It is as low as 7.1 per cent, whereas in the US and the UK it is 54 per cent and 57 per cent, respectively, according to an analyst with a leading broking house. Although the Sensex closed in red at 15590.42, down by 25.89, HDFC was among the top gainers on the index. LIC Housing Finance Ltd was up by 2.07 per cent and it has gone up 15.24 per cent on a week ago basis.</p>
        <p> Analysts feel that the housing finance sector is expected to do well as there is a growing need for housing in the country.</p>
        <p> “There is an increase in disposable income leading to more expenditure on housing leading to an ever-growing need for housing,” said Ms Sarika Lohra, banking analyst, Angel Broking Ltd.</p>
        <p> One more reason to housing finance companies better prospects is the dying interest of banks to provide mortgage loans. “The RBI has increased risk weights for mortgage loans which reduces focus of commercial banks in turn giving more opportunity to housing companies,” says Mr Saday Sinha, banking analyst Kotak Securities.</p>
        <p><b> Substantial buying </b><br>
          There has been substantial buying by FIIs and domestic institutional investors in both LIC Housing Finance and HDFC in the past couple of days, says Mr Jignesh Desai.</p>
        <p> “We feel future is quite bright looking at the problems abroad, Indian situation looks shielded probably because of the regulations formulated by the RBI and other institutions, also these housing companies are expected to do well as there is quite a lot of growth in the housing sector,” said Ms Anita Gandhi, Head of Institutional Business, Arihant Capital Markets Ltd.</p>
        <p> The lack of interest in other housing finance companies is due to factors such as low growth rates and a general low image of those companies unlike the top rung like HDFC which enjoys growth rate of 20-25 per cent, according to analysts. “Over a period of time as the housing sector picks up faster growth, the list of players in the housing finance sector is expected to increase,” adds Ms Gandhi. <br>
