  <div id="txt"><h1>Bank plans to add over 10,000 ATMs in next 3 years</h1>
  SBI plans to add over 10,000 ATMs to its network in the next three years to take its ATM network to 25,000. The bank currently has 7,200 ATMs. </p><p>

For the current year, SBI has identified 2,500 sites to set up ATMs, which include 700 railway stations. On Monday, the country's second-largest bank, ICICI Bank, had announced that it has received the central bank's approval to set up 2,500 additional ATMs.</p><p> 

ICICI Bank's ATM network currently stands at 3,600. SK Sehgal, general manager (IT), SBI said on Wednesday that the bank had seen a rise in the number of hits-per-day on its ATMs. "As compared to 130 hits two years ago, the average hits per ATM is at around 300 now," he explained. </p><p>

Average hits of 225-250 per day are considered optimum for ATMs, and this above-average performance has prompted the bank to go in for the expansion, he said.</p><p> 

Though he refused to divulge details on the amount of investment that will go in to the expansion, Mr Sehgal said the prices of ATMs have dropped from Rs 4 lakh a few years ago to Rs 3 lakh now, and that he was confident of getting machines at prices lower than that. </p><p>

"However, it is not just the cost of the machines, but the cost of real estate, power and maintenance that are the issues," Mr Sehgal added. SBI also plans to bring 100% of it's branches on the core-banking platform by March 2008. At present, the bank has about 65% of its 9,600-branch network on the CBS-platform. </p><p>

"We have 4,000 rural branches, which have been facing a lot of connectivity issues, but we plan to connect them as well via VSAT," he explained. 
