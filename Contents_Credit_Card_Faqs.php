    <div id="txt"> <b>What is a Credit Card, Charge Card or Debit Card?</b>
      <p>A <a href="http://www.deal4loans.com/credit-cards.php" title="Credit Card">Credit  Card</a>,  as its name suggests, gives you credit, for a charge, of course, and allows you to pay for service or product over a period of time. The days of credit one gets could range from 20 to 50 days (calculated from the day of billing and not from date of purchase) come interest free. You can choose to pay your dues entirely at one go or stagger them after paying the minimum amount due every month. A Charge Card is almost similar to a Credit Card, but with one key difference. With Charge Card, you have to pay the entire dues within the credit period and also you cannot carry over any balances like a Credit Card. A Debit Card is on the whole, an ATM card on the move.<br>
        It enables you to access your bank deposits for payment. When you make any purchases using a Debit Card, your bank account is instantaneously and automatically depleted to the extent of the purchase amount. <br />
        <br />
      </p>
      <b>How many types of cards can I choose from in India?</b>
      <p>In India there are essentially three types of cards available, Visa, Master Card and Amex (American Express). Participating banks like Citibank or ICICI then issue these cards to the subscribers. Both Visa and Master Card have been popular in India and have an almost equal market share and as for Amex, it is comparatively a new player in India and also it issues its cards only through American Express Bank.<br />
        <br />
      </p>
      <b>What is Gold, Silver or Classic/Executive Card?</b>
      <p> Gold, Silver or Classic/Executive are the terms used by issuing banks to distinguish between the levels of services offered on each. The Gold Cards fall on the premium segment as they feature better features like higher insurance covers, more privileges, more facilities, more discounts. These types of cards have the element of prestige and esteem associated with them. And they don't come any cheaper, as they have the highest fees and service charges.<br />
        <br />
      </p>
      <b>What is the process for owning a Credit Card?</b>
      <p> First get in touch with the card issuing bank. Ask for the application form. Fill-up the form and submit it along with the necessary documents required. After a short credit appraisal, the bank will send you your card by mail.<br />
        <br />
      </p>
      <b>What are the eligibility norms to get a credit card?</b>
      <p> Credit Card companies/ issuing banks do have a minimum income level requirement which serve as the preliminary point while <a href="http://www.deal4loans.com/apply-credit-card.php" title="applying for a credit card">apply for credit card</a>. The income  requirement varies from bank to bank, ranging between Rs.60,000 to Rs.2lac per  annum, depending upon your choice on the type of card you want and also upon  your risk profile. If you have a regular and steady income source, it helps, as  the banks issuing <a href="http://www.deal4loans.com/credit-cards.php" title="Credit Cards">Credit Cards</a> need  to be sure whether or not you will be able to repay the expenses incurred through your credit card. You have to be at least 21 years of age and not more than 65 years.<br />
        <br />
      </p>
      <b>What is the minimum salary criterion?</b>
      <p> A minimum salary criterion differs on the type of card you are applying for. You need a salary of at least Rs.70 to 75,000 per annum for ordinary cards and for a Gold card, up to Rs.1lac 80,000 per annum.<br />
        <br />
      </p>
      <b>How much does it cost to own a credit card?</b>
      <p> The basic cost involved in applying for a card comprises the joining fee, which varies from bank to bank and on the type of card you have applied for. Joining fees (a one time payment) vary from Rs.100 to Rs.1000. Every year you will be charged an annual fee to continue your membership on the card. Annual fees vary from Rs.400 to Rs.2000 The annual fee are payable in advance (at the beginning of the year) You do not need to pay cash for the annual fees as the issuing banks automatically bill these to your credit card in the first monthly billing statement, and hereafter, every year.<br />
        <br />
      </p>
      <b>Are there advantages in paying through a Credit Card even if I have
      cash I can spare?</b>
      <p> Depends on how you look at it.
        With a <a href="http://www.deal4loans.com/credit-cards.php" title="Credit Card">Credit  Card</a>,  you can delay payment of the bill by up to 50
        days. Also, most card issuers give you a discount on the next year's
        annual fees if you make purchases over a certain specified amount on
        your card. <br />
        <br />
      </p>
      <b>How are the fees levied when I delay my repayments?</b>
      <p> It is sensible to pay off at least the minimum amount due on
        out-standings every month. This is because the late payment fees are
        very high, around 15% of minimum payment due or 2.5% of the total
        outstanding balance. Late payment fees are chargeable after the
        stipulated 'interest-free period'.<br />
        <br />
      </p>
      <b>What is an Add-on Card/Supplementary Card?</b>
      <p> An Add-on card is usually for dependents of the Credit Card holder, be
        it your spouse, parents or children. Any additional cards under this
        head come at a fee, which varies between Rs.125 to Rs.1,000.
        One thing worth mentioning in a Supplementary Card is that your credit
        limit remains the same. A drawback is that your credit limit gets
        divided between the main card and add-on cards since an add-on card
        does not get additional credit limit.
        All expenses on the card are billed to you.
        On the statement date, you are given a comprehensive bill which
        includes the payment made through both the cards.<br />
        <br />
      </p>
      <b>What is a Global Card?</b>
      <p> Global Card enables you to use your Credit Card when you travel
        overseas. Global cards are accepted in over 200 countries. You can
        also use it as an ATM card. It also gives you the flexibility of using
        your card instead of using cash or traveler's checks.
        You can spend in dollars or any other foreign currency and settle the
        dues in your local currency.
        Your credit limit is based on the Basic Travel Quota (BTQ) entitlement.
        If you are a frequent traveler, abroad, then it is best to make your
        Credit card, Global.<br />
        <br />
      </p>
      <b>What is PIN?</b>
      <p> PIN or the Personal Identification Number is unique to every card and
        is a combination of numbers.
        It is basically a 16 digit number with the first 8 digits reserving
        information about the card issuer and the country and the next 8
        digits stores customer information like the category of the membership
        etc.
        The PIN serves as a key, which allows you to avail certain services
        like cash withdrawal from ATMs.
        This number is very important and must be stored carefully. Get Information on <a href="http://www.deal4loans.com/loans/credit-card/do-you-have-online-credit-card-pin/" title="Online Credit Card Pin">Online Credit Card Pin</a>. <br />
        <br />
      </p>
      <b>What happens if I lose my PIN?</b>
      <p> If you lose the PIN get in touch with the bank ASAP.
        The bank will replace your credit card and issue a new PIN.
        This replacement process comes at a cost and gets billed in your next statement. <br />
        <br />
      </p>
      <b>What is late payment fee?</b>
      <p> Late payment fees are payable on the occasion of any delay in
        repayment of the out-standings on your Credit Card after the
        predetermined interest-free period. These charges are very high
        (around 15% of minimum payment due or 2.5% of the total outstanding
        balance) and card members are well advised to pay off at least the
        minimum amount due on out-standings every month. <br />
        <br />
      </p>
      <b>What are Reward Points?</b>
      <p> The concept of <a href="http://www.deal4loans.com/loans/credit-card/credit-card-rewards-becoming-dearer/" title="Credit Card Rewards">Credit Card Rewards</a> Points was introduced by Credit Card companies
        to promote regular usage of their cards by its members. This concept
        basically rewards you with points for so-and-so amount spent on
        purchase or services through your card. These points can then be
        redeemed for gifts or for select product purchases or even for paying
        the annual fees in some cases.<br />
        <br />
      </p>
      <b>What are the various types of insurance cover available on Credit Cards?</b>
      <p> Insurance are now a standard feature for <a href="http://www.deal4loans.com/credit-cards.php" title="Credit Cards">Credit  Cards</a>, both personal
        and on the articles purchased on the credit card. The insurance cover
        provided is basically determined by the type of Credit Card owned,
        like depending on if it is Gold, Silver or Executive). Gold cards
        naturally carry the highest insurance cover for its members.
        
        Among the other types of insurance provided by the card companies are:
        
        Personal accident insurance:
        This covers air/road accidents etc. The amount insured again differs
        across the categories and varies from banks to banks.
        
        Baggage Cover:
        This provides cover against the loss of one's baggage while traveling.
        This feature is available on Gold Cards and International Credit Cards
        and frequent travelers may find this feature useful.
        
        Air Accident Insurance:
        Every Credit Card holder is insured for a certain amount of money,
        which is paid upon the death of the individual while traveling by air.
        The insurance cover varies between bank to bank and upon the type of
        card owned. <br />
        <br />
      </p>
      <b>What is the Life Insurance feature provided by Credit Cards?</b>
      <p> Every Credit Card holder is insured for a certain sum of money, which
        is paid upon the death of the individual. The insurance cover varies
        from bank to bank and depends upon the type of card owned.
        The card holder is insured a certain amount against different life
        insurance covers depending on the category of the card owned. <br />
        <br />
      </p>
      <b>What kind of insurance is given for a spouse/supplementary card holder?</b>
      <p> Credit Card companies also provide insurance covers for a spouse or
        the holder of a Supplementary/Add-on card. The amount and the type of
        insurance cover provided are basically determined by the type of
        Credit Card owned (Gold, Silver, and Executive)
        Gold Cards carry the highest insurance cover for its members.
        The insurance cover varies between Rs.50,000 to Rs.500,000 depending
        on the issuing bank and the type of card owned. <br />
        <br />
      </p>
      <b>What does the purchase protection feature of Credit Cards mean?</b>
      <p> Purchase protection feature of Credit Card means all items bought from
        the card are automatically insured from damages or loss due to
        theft/fire, up to a certain amount.
        This generally works out to around Rs.40,000 worth of cover. <br />
        <br />
      </p>
      <b>Is it wise to use the cash advance facility?</b>
      <p> Cash advance (withdrawal) fees are payable when you withdraw cash from
        an automatic teller machine using your Credit Card.
        Cash advance facility should be used only when it is extremely necessary.
        The interest rate is very high, usually around 36% to 38%.
        Cash advance fees are chargeable on a daily basis and at about 2.5%
        per transaction of fixed at a minimum of Rs.50 or 100; it is wise not
        to use this facility if you can manage otherwise. <br />
        <br />
      </p>
      <b>How should I protect my credit card from abuse?</b>
      <p> Never treat your card carelessly and always keep it safely in your wallet/purse.
        Never leave your cards unsigned or disclose your card number.
        Check your name on the card, note down the card number and sign below
        the magnetic strip.
        Always keep the copy of signed charge slips when you spend on your card.
        This will help you tally what you spent with what you get billed for.<br />
        <br />
      </p>
      <b>What should I do if my credit card is lost or stolen?</b>
      <p> Incase you lose or your card is stolen, inform the bank immediately.
        The bank will then immediately freeze/deactivate your card to prevent
        any fraud.
        After you report the loss, your liability is restricted to Rs.1,000.
        You may also have to pay for the reporting of the loss in the lost card list.
        Besides this, you will be expected to pay for the issue of a replacement card.<br />
        <br />
      </p>
      <b>How much risk in the event of losing the credit card, am I looking at?</b>
      <p> EYou are protected from settling any expenses on your card the moment
        you inform the bank, plus your liability will be restricted to
        Rs.1,000.
        But you will have to pay for all the purchases deceptively made on your card. </p>
