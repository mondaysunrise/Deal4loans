        <p> Manmohan Huria, a retired IAS officer from Punjab, cautions that it would not be fair to generalise that everyone in this segment is a bad risk. </p>
        <p> Some borrowers in this age group do find themselves over-leveraged, Huria adds. The onus is on the banks to give credit to this segment in smaller doses if they discipline credit. </p>
        <p> It is for the <a href="http://www.deal4loans.com/loans/banks/">banks</a> to observe all due diligence practices. Earlier, it was the banks that were giving loans to young borrowers, by including the income of parents, just to boost the housing loan portfolio. </p>
        <p> Now, a sizeable number of private and public sector banks have decided not to give loans to those less than 25 years of age as there is a significant change in borrower behaviour between the age of 26 and 28. Despite the reduction the rate of interest and number of new schemes being floated by the banks - housing loans are not picking up. <a href="http://www.deal4loans.com/home-loans.php">Home loan</a> growth is expected to fall to 15 per cent after two years of robust 33-35 per cent growth. </p>
