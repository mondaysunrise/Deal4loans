        Banking industry sources have for the time being cautioned that a combination of rising interest rates, high cost of real estate and higher margins in bank loans would slow down the growth in home loan portfolio. Even now the RBI is not at all interested in bringing down the interest rate on housing loans but banks on their own are deciding to lower the interest rate because of the competition.<br>
