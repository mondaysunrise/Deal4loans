  <div id="txt"><h1>Shuffle your plastic</h1>

With the festive season kicking in, banks and credit card companies are offering ‘cash backs’ to woo you. Smart shuffling of plastic could save quite a sum for shoppers. <br><br>
<b>How to smartly use cash backs?</b> <br>

• Check if the offer is valid across all categories.<br> 
• Check if it’s applicable for your card.<br> 
•  Read the conditions — they are quite different for various cards .<br>
• Register for cash backs. Some banks offer cash back as an optional facility .<br>
• See if there are any ceilings for cash backs to avoid surprises later <br>
•  Keep an eye on the validity of the offers .<br>
•  All things remaining same, use credit card instead of debit card as the former gives free credit 
<p>
For Indians, the fag end of the year is time for spending. There are clothes to be bought for the family, a snazzy mobile for dear sister, Faber chimneys to cool the hot tempered domestic help, a 5.1 channel DVD player for son and so on. The spending list is endless and often emotions rule during this time. </p>

<p>While it might be difficult to cut the shopping list, one could at least slash costs. This is all thanks to banks and credit card companies which are coming out with a myriad promotional offers including the ‘ubiquitous’ cash backs. Smart shuffling of your debit and credit card could actually save quite a sum. 
</p><p><b>What are cash backs? </b><br>

Cash backs refer to the process of banks and credit card companies offering you cash back — based on the amount spent through the credit or debit card. For instance, there could be a 5% cash back, which means Rs 5 would be credited back to your card for every Rs 100 spent. </p><p>

The catch, though, is that there are often ‘conditions’ to be met. For instance, there could be a ceiling on cash back amounts, limitations on its usage (like some specify it is applicable only where the card-issuing bank has installed terminals), time limitations, line of products where it is applicable and so on. </p><p>

Take for instance the HDFC Bank’s recent advertisement, it says, “You can feel 5% less guilty.” It means, if you shop by swiping your HDFC Bank debit card, you can enjoy up to 5% cash back on your ‘highest’ transaction. The key word here is the highest. Further the cash back is different for every month. </p><p>

For example, if you are transacting in all three months (October, November and December in this case), you will be eligible for 5% cash back. If you are transacting in any 2 months, the cash back is capped at 3% and 1% for any one month. The offer is valid till December, it mentions. </p><p>

Similarly, the HSBC debit card offers 5% cash back on spends up to Rs 5,000. The minimum transaction amount should be Rs 2000 in the offer, which spans from October 6 to November 30. Standard Chartered Bank has worded it differently. </p><p>
They are offering 2 reward points for every Rs 75 spent on a priority debit card and Rs 125 for a smartfill debit card instead of the usual 1 reward point. </p>

<p>
		
Again in this case you have to spend a minimum of Rs 5,000 in the offer period. You can earn a maximum if 300 points, which can be redeemed in form of cash by calling the bank’s phonebanking number. Even ICICI Bank has announced an array of offers for its debit card holders. But the offers are limited to few stores depending upon which zone you belong to. All the concerned details are available on the bank’s website. </p>

<p><b>Things to watch out for </b><br>

Often banks advertise cash back offers on billboards, magazines or newspapers. But you should crosscheck if the same offer is available on the card you hold. If it’s applicable then what are its other terms and conditions are. If you read the HDFC Bank’s advertisement on bank’s brochure or on the website, it clearly states, the cash back offer is only valid on debit card transactions above Rs 1,000. </p>
<p>
Secondly, maximum cash back is limited to Rs 250 per month. So even if your highest debit card spends is above Rs 5,000, you can get only the stipulated amount. The cash back will be credit at the end of the period i.e. December. Lastly this offer is not valid on cash withdrawals from ATMs through debit cards. </p><p>

Banks kick off these cash back offers only in the festive season, which starts from October and goes on till December end. So always keep an eye on the validity of the offers before you loosen your purse strings. </p>
<p>
Many banks offer this privilege to only selected card-holder categories. Similarly some banks offer cash back as an optional facility for customers. In such cases you have to call the phone banking or fill an online form to sign up for this offer. </p>
<p>
One more thing to watch out would be if the offer applies at all outlets having point-of-sale terminals of different banks. Some banks insist the purchases should be made only where the card-issuing bank has installed such terminals. <br>

Similarly you should see if the offer is valid across all products categories. </p><p>

Lastly, check the offer period, the minimum and maximum limits on which cash backs are available. Spending isn’t necessarily a bad thing if it could bring smiles to your near and dear ones. Ensure that the bunch of plastic that usually fills your wallet is smartly shuffled and used to cut your shopping bills this season. 
