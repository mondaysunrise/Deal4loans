          <br>
          &quot;The main reason for this increased use of plastic for payment is the  spread of debit cards in India,&quot;  said Piyush Khaitan, VC &amp; MD, Venture Infotek. &quot;Consumers find that  debit cards relieve them from the additional chore of drawing cash from ATMs.  Now they can spend from the money parked in their bank account  directly at the store,&quot; he added. <br>
          <br>
