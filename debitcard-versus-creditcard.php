      <div> Blame it on the fear of credit  default or the age-old Indian mindset that does not consider taking a loan a  good option if one has cash in one's account. Payment by  debit cards in India  is rising at a faster clip compared to spending through credit cards. <br>
        <br>
        While data from companies involved in facilitating card payments show that  there has been a rise in number of payments using credit and debit cards, RBI data available for the  first seven months of the current financial year also validate claims of  increased spending using plastic cards. However, it's the use of debit cards  that's growing at a faster rate than credit card payments.<br>
        <p>RBI data shows that during the first seven months of the current financial year  (2008-09), payments through debit cards crossed 80% of the total transactions  recorded in 2007-08. Better even is the value of transactions through debit  cards: 84% of the value of total transaction during the whole of FY08 was  already achieved between April and October of FY09.</p>
        <p>While nearly Rs 10,500 crore  worth of transactions occurred during April-October of FY09, during the whole  of FY08, total spending through debit cards was a little over Rs 12,500 crore. <br>
