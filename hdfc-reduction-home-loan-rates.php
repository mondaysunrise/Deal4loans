<p> A day after Finance Minister P Chidambaram pitched for lower interest rates on 
<a href="http://www.deal4loans.com/home-loans.php">home loans</a> up to Rs 20 lakhs, private sector housing lender HDFC on Friday said it was not possible for now to further slash lending rates. &quot;If you ask if there is any possibility of reducing the interest rate then I will say no right now... because in the month of March, the market becomes tight due to corporate tax payment,&quot; <a href="http://www.deal4loans.com/loans/banks/hdfc-bank-hdfc-ltd/">HDFC</a>  Joint Managing Director Renu Sud Karnad told reporters here on Friday
  </p>
  <p>When asked if she saw any scope for reduction in interest rates, Karnad said: "I cannot say on this with certainty... but it is for sure that interest rate will not go up further." </p>
  
<p>Last month, HDFC lowered its prime lending rate by 0.25 per cent to 13.25 per cent. 
  
  HDFC, having 0.92 per cent default rate, is looking at a growth of 25-30 per cent during next fiscal on the back of boom in housing construction activity in the country. "We will achieve 25-30 per cent growth in the coming fiscal in terms of 


  <a href="http://www.deal4loans.com/">loans</a>  disbursement," she said
  </p>
  
<p>HDFC, which claims a 38-39 per cent share of the 


  <a href="http://www.deal4loans.com/home-loans.php">housing loan</a>  market, is certain of achieving loan advances of Rs 30,000 crore by this fiscal end, up by 25 per cent compared to last fiscal. </p>
  
The lender, which is organising a property exhibition here, is offering reduced interest rate for the duration of the fair. 
  
  Giving details, she said that interest rate of 10 per cent would be charged on housing loans up to Rs 20 lakh and 10.15 per cent for more than Rs 20 lakh loan requirement. "Presently, we are charging 10.25 per cent on housing loan." 
  
  She said, "The expo is an attempt to provide home seekers all the services required for property purchase under one roof." 
