	<p>
Reverse mortgage loans may become cheaper if the Reserve Bank of India (RBI) agrees to lower the risk weightage on such loans. Currently, there is some ambiguity on whether the reverse mortgages should be treated as personal loans or as retail loans.</p>

<p>Sources said Indian Banks’ Association has sought clarification from RBI on this. As per capital adequacy rules, on a loan of Rs 100, a bank has to set aside Rs 9 as capital if the risk weightage is 100%. However, the capital that needs to be set aside could be lower if the risk weightage is lowered.</p>
<p>
As per current guidelines banks have to assign a risk weightage of 125% point on the personal loan against 75% on retail loans. Since reverse mortgages are loans will be availed by individuals and the borrowed amount would be lower in the range of Rs 1 lakh to Rs 50 lakh it could fall in personal loan category. </p>

<p>
At present any general purpose loan taken by an individual is treated as a ‘personal loan’ since the borrower need not reveal the purpose — unlike in housing or education which are also given to individuals but for a specific purpose.</p>

<p>
Bankers feel that instead of classifying reverse mortgage as personal loans, these should be encouraged by granting it the priority sector status because the scheme has social angle to it. The loan does not generate any income for the bank for 15 years. Even if it has to sell the property on the death of the borrower, the proceeds in excess of the loan amount, are transferred to the nominee.</p><p> 
Under the scheme, senior citizens can consider earning a monthly income by pledging their home with a housing finance company (HFC) or a bank even while they continues to occupy the house for lifetime. The borrower is not required to service the loan during his lifetime or make monthly payments.</p>

<p>On the borrower’s death or on the borrower leaving the house property permanently, the loan is repaid along with accumulated interest, through sale of the house property. Alternatively, the borrower or its heir can repay or prepay the loan with accumulated interest and have the mortgage released with outresorting to sale of the property.</p>
<p>SBI has a margin of 10%, but it is charging 10.75%, PNB is charging 10% and a margin of 20% and Allahabad Bank is charging 11% and a margin of 40%. Union Bank is charging 10% with a margin of 30%. All banks are charging a fixed rate, subject to a reset after five years. The monthly installment paid to the customer will factor the interest component. Among non-banks LIC Housing Finance and Dewan Housing have announced their reverse mortgage schemes. 
