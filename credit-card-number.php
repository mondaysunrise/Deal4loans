  <b>Information on the stripe</b><br>
    Strip of magnetic tape, affixed to bank credit and debit cards, encoded with cardholder identifying information, such as the Primary Account Number and card expiration date, permitting automated handling of transactions. The card industry standard for magnetic stripes allows three separate tracks of encoded data and each track is about one-tenth of an inch wide. The ISO/IEC standard 7811 is used by banks; specifies:<br><br>
    • Track one is 210 bits per inch (bpi), and holds 76-alpha numeric data characters. <br>
    • Track two is 75 bpi, and holds 37 Numeric Data characters.<br>
    • Track three is 210 bpi, 104 Numeric data characters.<br><br>
    
