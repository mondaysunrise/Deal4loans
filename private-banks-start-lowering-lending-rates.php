        For Axis Bank and Bank of Rajasthan, the new rates would be effective from April 1.<br>
        <br>
        Axis Bank President (Credit) Partha Mukherjee said the cost of funds for the lender had come down over the past few months. “Interest rates in the system have come down and so have our cost of funds. So, we have decided to pass on the benefits to our customers,” he said.<br>
        <br>
        Since October last year, the central bank has cut the cash reserve ratio, or  the proportion of deposits that banks set aside, and the repo rate, or the rate  at which it lends to banks, by 400 basis points each to inject liquidity into  the system and signal a lower interest rate regime. <br>
        <br>
        In addition, the reverse repo rate has been lowered by 200 basis points to  discourage banks to park surplus funds with RBI. So far in 2009, the CRR has  been lowered by 50 basis points, while the repo and reverse repo rates have  pared by 150 basis points each. Public sector banks have responded by pruning  their lending rates by 150-200 basis points but the private sector lenders had  not cut rates.<br>
        <br>
        In the recent weeks, the government and RBI have prodded private and foreign  banks to lower rates. While some private banks have shown movement, the foreign  banks, barring Citibank (which pared <a href="http://www.deal4loans.com/home-loans-interest-rates.php">home loan rates</a> by 50 basis points to  13.75 per cent), are yet to take the initiative.<br>
