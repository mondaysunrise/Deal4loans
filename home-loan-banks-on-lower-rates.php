        <p> Home loan borrowers are increasingly shifting loans from private sector mortgage lenders to public sector banks (PSBs) to take advantage of the wide difference between the interest rates for existing borrowers of private lenders and new borrowers of PSBs,said executives of two public sector banks. </p>
        <p> Take the case of Rajat Sharma (name changed), a private bank customer. He took a Rs 35 
          
          
          lakh <a href="http://www.deal4loans.com/home-loans.php">home loan</a> two years ago and now owes a principal amount of Rs 30 lakh, after paying EMIs and making part pre-payments. Mr Sharma is now shifting his loan to the State Bank of India (<a href="http://www.deal4loans.com/loans/banks/sbi-state-bank-of-india-loan/">SBI</a>), the country’s largest bank. </p>
