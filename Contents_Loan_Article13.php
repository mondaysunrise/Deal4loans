        <p> With export growth likely to moderate this year on the back of weakening US demand and a subsequent global economic slowdown, lower interest rates may help to prevent a loan crisis in the emerging powerhouse, said Moody's Economy.com, a subsidiary of global credit rating agency Moody's Corporation.</p>
