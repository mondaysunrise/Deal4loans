  <div id="txt"><h1>Citi not to exit India finance business</h1>
<p>
You think nothing comes for free in this little imperfect world?? Have a look at your credit card statement again. Reward points are earned with every purchase made with the   <a href="http://www.deal4loans.com/credit-cards.php">credit card</a>. These reward points can eventually be exchanged to make purchases, or redeemed from the bank in the form of gifts. </p>
<p>
Reward Points bring back memories of school where we used to get rewards for good deeds. But how are good deeds defined in this context? Well, banks define them as the frequent use of   <a href="http://www.deal4loans.com/credit-cards.php">credit cards</a>. The more you use the merrier you will be. The theory behind reward programs is a simple one. Banks want to encourage you to use the card as much as possible so they receive higher amounts from participating merchants. The benefits to cardholders are obvious - you get something for nothing.</p>
<p>
There are typically three types of reward programs: point-based programs, cash-back programs, and frequent flier reward cards. Programs that are point-based, let you earn points with every credit purchase.  These are programs that allow your credit purchases to provide you with points that can be redeemed for merchandise, services or transferred to frequent traveler programs of  hotels or frequent flier programs of various airlines.</p>
<p>
If you consider one such card, be sure that the rewards offered are on commodities or services that you actually want and will use.  Also, keep in mind that these points usually have an expiration date. Point-based programs may not always prove to be of good value, since reward items are often grotesquely overpriced.  You may have to spend far more than the monetary worth of the reward.  </p>
<p>
Awarding of points for these purchases may vary depending on the items you buy, the special features of your credit card and the reward items that your accumulated points can redeem. For Citibank cardholders, every 100 rupees worth of purchases translate to one point. Standard Chartered card holders, on the other hand, have to spend 125 rupees to gain one reward point. </p>
<p>
The numbers of reward points you have accumulated are reflected in your monthly credit card statement mailed to you by your banks. You can then choose your reward through a catalog or a website.   <a href="http://www.deal4loans.com/loans/banks/">Banks</a>  usually provide you with reward catalogues where you can choose from products currently on offer from the bank. This can be done offline as well as online. Sometimes the redemption of points is possible at various outlets, e.g. An ABN AMRO BARISTA CARD will let you redeem your reward points instantly at Barista outlets.</p>
<p><b>An example of reward points in the form of air travel miles</b><br>

There are several credit cards on offer that issue reward points in the form of air travel miles. As a card user makes regular purchases, equivalent points in the form of travel miles are awarded. The air travel mile points accumulated over credit use don't necessarily equal actual miles required to make the travel.  It usually depends on the destination, the bank offering the travel programs and the airlines that the credit card companies are working with. </p> 

<p>Frequent travelers can benefit a lot from the use of their credit cards if they opt to redeem accumulated points as air travel miles. You can redeem your travel points against travel promotions in place by the bank or keep on accumulating point to take a planned vacation in the future. You will have to monitor your accumulated points to determine how many points you need in order to get that vacation. These points can also be redeemed against plane tickets.</p>

<p><b>What is a good strategy for maximizing the number of reward points?</b><br>
Listed below are some tips for getting the most out of reward points:<br>
&bull; Pool your points: If you or your spouse have a rewards credit card, request a companion card from their bank so the two of you can team up to earn more rewards by adding points.<br>
&bull; Use your card to pay for necessary spending such as paying for groceries. This can really help boost the volume of your points.<br>
&bull;	Pay your household bills online with a reward credit card. There is a lot of opportunity for earning points back for paying your telephone bill, electricity bills; Internet service provider, hospitals, etc. so don't waste this points earning capability by writing a cheque to pay the bill instead. </p>
<p>
Reward Programs can be cancelled or altered at any time, so it is suggested to select a program with a proven track record. Also, frequent flier miles and other points sometimes expire, so make sure that you can realistically accumulate enough within the given time period to receive your reward. </p>
<p><b>Simply put: "Points are a form of currency. A Point earned is money earned.</b><br><br>
With all of that said, it is recommended to pay off your complete balance every month to avoid any carrying over. Paying exorbitant interests can and in most cases will, completely negate the benefit of reward points.<br />
