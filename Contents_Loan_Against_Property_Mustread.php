<h1 class="lap-h1">Loan Against Property Mustread</h1>
<p>Loan against Property refers to the secured loan  category like <a href="http://www.deal4loans.com/home-loans.php" title="home loan">home loan</a> where the borrower gives a guarantee by using  his property as a security. The right of ownership of the property is still  with the borrower, and if he/she is unable to repay the loan amount, he/she can  sell the property to pay off the debts. <br>
<br>
<b>Typically these loans are used to start or expand business or to renovate your house. But it can also be used to repay existing high rate loans.</b>
<ul style="margin-left:25px;">
<li> The maximum loan amount varies from bank to bank. It could range from Rs.2 lacs up to Rs.100 lacs. The exact amount depends on your property valuation, income and of course repayment capacity.</li>
<li> The rate of interest is usually 6.5%+, but depending on one's profile and the Bank's criteria, it may vary.</li>
<li>  The maximum loan amount can come upto 50% of property value for commercial setups and up to 60% for residential properties.</li>
 <li>The maximum loan tenure is 15 years.</li>
<li>  You require security, collateral or guarantors for obtaining a Loan Against Property.</li>
<li>  Most banks do not accept properties that are on lease or that are based on power of attorney.</li>
 <li> The maximum age limit of eligibility is 60 years.</li>
<li>  You can choose either Fixed or Floating rate of interest. You also have an option of 
  changing from Fixed to Floating interest rates and vice versa once every year.</li>
<li>  A processing fee is usually 0.05% to 3% of the loan amount and is payable upfront. This fee however will be deducted from the disbursal amount payable to you. You should always ask for the 0% processing fee or negotiate the processing fees.<br></li>
 <li>You pay your loan in EMIs through post-dated cheques or through ECS to debit your Bank account through ECS with the EMI amount.</li>
  <li>You can also prepay the entire loan outstanding anytime after 180 days of availing the loan. Pre-payment charges will be levied accordingly. If you intend to do so, please ask for the pre-payment amount to be waived or a reduction in the penalty charges.</li>
<li>  You can also increase or enhance your loan <a href="http://www.deal4loans.com/Contents_Loan_Against_Property_Eligibility.php" title="loan against property eligibility">loan against property eligibility</a>. For that you need to show income of atleast three persons, most preferably a family member or a business partner. </li></ul><br />
 
  <b>Also know these pointers before availing a LAP:</b>
<ul style="margin-left:25px;"><li>
  Decide on the basis of what you really need. Also see if the cost fits into your 
  estimated budget. </li>
<li>  Compare the quotations given and interest rates from 3-4 banks, select the one which 
  offers maximum benefit and serves your purpose. </li>
<li>  Also determine the tenure of the loan. The EMI may come less for longer tenure, but the 
  total interest outgo will be higher. </li>
<li>  Know all about processing fees and time. Some banks may waive the processing fee for 
  processing loan but they build this cost on their interest rates. </li>
<li>  Consider pre-payment options. All banks charges 2% - 3% of the loan in case you decide 
  to pre-pay the outstanding amount. </li>
<li>  Default in payments results in penalties. It can also adversely affect your credit 
  history and profile. So make sure to make your payments on time. </li>
<li>  Make sure that all deals and offers agreed upon are supported by relevant papers. So make sure you always ask for a letter in a banks letter-head mentioning the likes of, exact rate of interests, processing fees, pre-payment charges along with interest-schedule. </li>
<li>  Also before signing the documents, make sure you recheck all terms and conditions. </li>
<li>  Do not at any circumstance give any false information. This may amount to fraud and could land you in trouble. </li>
<li>  Do not sign any blank documents. Even if it takes you a few hours to fill-up the form, 
  please do so. Do not leave anything for the executive to fill-up. </li>
  <li>Finally, once you have received a loan do your best to pay it back as quickly as possible. Banks make their money off the interest they charge and the sooner you pay back a loan the less money you will have to pay in interest. </li></ul>
