<p>

Most consumers risk being hit by hefty fees or a nasty spike in interest rates if 


  <a href="http://www.deal4loans.com/">loan</a>  or 


  <a href="http://www.deal4loans.com/credit-cards.php">credit card</a>  repayments are late -- but in India there is also the danger of being pummelled with an iron bar. 
</p>
<p>
Consumer lending has taken off here, but few have credit histories that make it easy to weed out risky borrowers, so mainstream banks have resorted to other tools to keep defaulters on their toes. 
</p><p>
The tools are small, unregulated loan recovery agencies -- whose tactics can include public shaming, kidnapping, death threats and even beatings. </p><p>

Vinod Chaudhary, a 22-year-old student, happened to be in the car of a family friend who had fallen behind on repayments when he experienced loan recovery, Indian style. </p><p>

"They grabbed my collar and started beating me. Someone hit me from behind and I almost passed out," Chaudhary said of his close encounter with agents, who were armed with an iron bar in their operation to recover the vehicle. </p><p>

The car's owner and defaulter, Tapan Bose, sued ICICI, India's largest private bank. In November the bank was fined Rs 5.5 million (138,000 dollars). </p><p>

ICICI declined to comment on the case, which is still in appeal. In another case, the bank paid up Rs 1.5 million in damages after a father-of-three from the financial hub of Mumbai committed suicide, blaming threats from ICICI's recovery agents. </p><p>

But ICICI, which accounts for a third of all consumer 


  <a href="http://www.deal4loans.com/">loans</a>  in India, is far from alone in using the heavy-handed agents. </p>
<p>

A Mumbai branch manager for HDFC Bank, India's second-largest private bank, and two others were arrested by police in October for threatening to murder a customer who had defaulted on a 5,000 rupee loan. </p><p>

The Reserve Bank of India is now circulating a draft of new guidelines on using recovery agents, a job that sprung from the less than decade-old boom in consumer lending in a fast-growing economy. </p><p>

Until six or seven years ago, institutional consumer lending barely existed and Indians mostly borrowed from friends, family, or a neighbourhood money lender. </p><p>

The arrival of private banks in the last decade has changed all that, and 


  <a href="http://www.deal4loans.com/personal-loans.php">personal loans</a>  currently account for a quarter of India's 500 billion dollars of bank lending. Last year the sector grew by 30 per cent. </p>
<p>

During the holiday season, banks hold "loan festivals" urging borrowers to apply for credit. That credit has been good for the economy, financing the purchases of cars, motorcycles and other goods. 
</p><p>
But more loans means more risk -- and debt lawyers say that even though the justice system can deal with cases of large debt, defaults on loans of a few hundred dollars are viewed as scarcely worth the trouble of going to court. </p><p>

That's where the collection agents come in. A New Delhi-based company, the DNL investigation agency, boasts on its website that 70 percent of cases it has dealt with are "resolved out of the court." 
</p><p>
"We apprise the defaulters that their escape routes would be quickly closed," the company says. The agency's founder, Subhash Dutt, declined to be interviewed on what exactly this entailed. 
</p><p>
Banks say they do their best to weed out risky customers but point to the lack of credit histories. 
</p><p>
"There's a gap in Indian law. We don't have any records of the borrower. We don't have any credit control system. We don't have any credit reporting system," said lawyer Kaviraj Singh, whose firm Trustman Associates specialises in debt collection on behalf of foreign clients. </p><p>

"In the US, if a lender enters your name in a database and you have a 

<a href="http://www.deal4loans.com/credit-cards.php">credit card</a> , the bank will come to know if you are paying. Or if you are not paying." 
</p>
<p>
Action is being taken with the Indian government setting up a specialised credit agency -- but in a country of 1.1 billion people, the task of building up an effective database will be colossal. </p><p>

"Apart from the 50 million people who have credit, no one else has any record," commented a senior private bank official, who asked to be named. <br/><br/>

"India is going through the same stage that many developed countries went through 15 to 20 years ago. It's going to be a long haul." 
