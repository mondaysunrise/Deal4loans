    <div id="txt"><h1>Journey to Home Loan!</h1>
<p>Planning to buy that long cherished dream home?? Well congratulations, in today’s day and age, just talking about buying a home can be a great source of excitement for a first-time homeowner. However often the reality of the situation overshadows the magic of the experience. Getting to understand all the options available and choosing the one most suitable can be a daunting task. Unlike before the availability of different sources of finance, in itself is one of the chief reasons people prefer to buy homes as opposed to rent them.
A good home loan scheme can really smoothen the way between you and your dream home. It is therefore crucial that you as a buyer have your basics on home loans clear. This will keep you from making some very expensive oversights during the purchase of a first home. Here are some basic “must-knows” about home loans.
<br><br>
<font color="0F74D4"><b>What kinds of home loans are there? </b></font> <br>Home loans, home extension loans, home improvement loans, land loans, NRI loans, home equity loans, short-term bridge loans, converting high-interest loans, and low-interest housing loans. </p>


<p>
<font color="0F74D4"><b>Who can get a home loan? </b>
 </font><br>
 If you are an Indian resident or a non-resident individual, you can apply for a housing loan. In fact, you can also apply for a loan even before you've selected your property. 
 </p>

<p>
<font color="0F74D4"><b>How much can I get as a loan? </b>
 </font><br>
 Housing finance companies do not bankroll the entire value of the property; you will need to chip in about 15-20 per cent of the total amount. 
Besides, the loan amount a finance company will agree to will usually range between two and three times your annual household income. If you were married, a good idea would be to add on your spouse's income with your income to improve the chances of receiving a larger loan. 
While you're figuring out your ideal loan size, it's a good thing to think about your repayment capacity: loan borrowers have to repay a part of their loan every month in what are called 'equated monthly installments.<br><br>

Usually, it doesn't exceed 50 per cent of your monthly household income. <br><br>

Don't be timid when trying to understand the nitty-gritties of taking a housing loan. Ask questions if there are details you don't clearly understand, especially about legal matters such as what the company can do if you don't pay up on time (even once) or if you don't take up the loan before the stipulated time-frame. 

</p>


<p><font color="0F74D4"><b>What factors do housing finance companies/banks look at before sanctioning a housing loan? </b> </font><br>
In one word, credit-worthiness. Your credit-worthiness will depend on various factors such as age, qualifications, employment, work experience, dependents, previous track record of repaying any earlier loans you may have taken, etc. 
You will also need to provide a variety of personal and property documents. These could include salary slip, copy of income tax returns, etc. Property documents include original sale agreement, original receipts of payment made by the property to the builder, and other legal documents. 
The age of the property is also important - if it's more than a decade old, not too many companies will be in a hurry to finance it. </p>

<p><font color="0F74D4"><b>How do I pay the installments?  </b> </font><br>
Loans can be repaid either through a deduction against salary, post-dated cheques, cash or demand drafts, or any other mode decided by your loan providing company.  </p>

<p><font color="0F74D4"><b>What is 'interest rest'? What is the difference between an annual and monthly rest?  </b> </font><br>
Interest rest can be defined as the principal outstanding on the date, which is considered to calculate the interest. When we say monthly reducing balance, it would mean that interest is calculated on the principal outstanding at end of every month after taking into consideration payment of the EMI. 
For the same loan amount, tenure and interest rate, EMIs for monthly rests are lower than they are for a plan with annual rests. 
  </p>
  
  <p><font color="0F74D4"><b>Other than the interest, what are the other costs I have to take into account when considering a housing loan?  </b> </font><br><br>
There are many fees and charges that add on to your effective cost of taking a home loan. The important ones include: <br><br>
<font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Processing fees:</b></font> This is levied to cover costs incurred in processing your loan application. You will need to pay this when you submit the application form. Usually, it is around one per cent of the loan amount. Some finance companies may charge a flat fee instead.  <br><br>

<font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Administrative fee:</b></font> This again, may be a percentage of the amount sanctioned. You have to pay this fee when you accept the offer letter of the loan provider.   <br><br>


 <font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Legal charges:</b></font> Some companies may also insist that you pay for the legal expenses they incur for getting your property documents scrutinized by their lawyers.    <br><br>
<font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Technical charges: </b></font> A charge to meet expenses on technical site visits to your property.    <br><br>


<font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Stamp duty and registration charges</b></font>   <br><br>


 <font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Personal guarantee form charges: </b></font> Some finance firms will demand a personal guarantee. These need to be stamped, and the finance company will also charge this to you.    <br><br>
<font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Cheque bounce charges: </b></font> If the cheque you wrote out to pay your home loan installment is dishonoured; you will be asked to pay a charge as penalty.     <br><br>

<font face="Verdana" size="1" color="0F74D4">•</font> <font color="0F74D4"><b>Delayed payment charges: </b></font> If you delay making a payment beyond the due date, some finance firms will penalize you for that. 
  </p>

<p>Besides, some housing finance companies insist on the borrower taking out an home insurance policy -- with the loan provider being made the co-beneficiary-- before agreeing to hand down a loan. </p>
<p>Not all housing finance companies will refund the fees if you cancel the loan after accepting the offer letter -- some may retain a portion of the processing charges. You can request an increase/decrease from the original loan at any point of time. </p>
<p>If you've asked for an increase, you may be charged a fee. If you're looking at reducing your loan amount, the higher fees paid earlier is usually adjusted in future payments. 
Well that’s all for now, hopefully these tips will go a little way towards helping you get that dream home soon!

