  <div id="txt"><h1>Transparent pricing of loans</h1>
  <b style=" font-size:11px;  font-family: Verdana, Arial, Helvetica, sans-serif;">MONETARY POLICY MID-TERM REVIEW 2007-08/ SUPERVISION AND REGULATION</b><br><br>


In a move to make pricing of loans more customer-friendly, RBI has advised the banks to be more transparent and fair in fixing and revising interest rates on housing loans. </p><p>
 
Y V Reddy, governor, RBI, said, “On an informal level I have told bankers that on repricing of housing loans, there has been a lot of criticism. And we may have to take a strong view if banks do not ensure fair contracts and fair pricing.” </p><p>
 
The immediate reason for this seems to be the fact that banks have raised their floating home loan rates by as much as 4 percentage points over the last one year. Fixed rate home loans carry a reset clause after two-five years depending on the tenure of the loan. </p>
 <p>
“We have got certain complaints about non-transparent pricing that is why we want banks to assure us that it is not the case. </p><p>
 
Even fixed rates, apparently have a clause, which says it may be changed if something happens. So the so-called fixed rate is not all that fixed. Hikes have to be reasonable. That is what we want,” said Reddy.</p> 
 <p>
Though some of the banks have reduced rates as a part of the festival offers, the apex bank has pointed out that the cuts are only for the new customers. </p>
 <p>
Currently, the existing borrowers have the choice to either move to another bank offering lower interest rates or pay a charge and close their existing loan account and open a new loan account with the same bank.


<br><br>
<b>Source :</b> Business Standard</p>

