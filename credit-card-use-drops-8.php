        "Due to the economic downturn, people are restricting unnecessary spending," a public sector bank executive added.<br>
        <br>
        "Consumer spending on almost all fronts has come down, and there has also been a lot of culling in the industry, Sujan Sinha, senior vice-president for retail banking at Axis Bank, said. Banks have also reduced the total number of credit cards in circulation by 282,000 during January to check losses arising from the portfolio.<br>
        <br>
        According to latest data released by the Reserve Bank of India, the credit card population fell by 8.62 per cent to 25.87 million at the end of January this year, as against 28.31 million at the start of the financial year. This is in contrast to April-January 2007-08, when the number of credit cards rose by 13.32 per cent.<br>
        <br>
        "Credit cards of marginal customers and defaulters have been cancelled, and credit appraisal mechanisms have also become more elaborate. NPAs for almost all players in the industry have risen," Axis Bank’s Sinha said.<br>
        <br>
        Standard Chartered Bank’s country head for retail banking Shyam Srinivasan said that his bank had seen a more or less flat credit card population and did not intend to step up activity given the present economic situation.<br>
        <br>
        With non-performing assets in the card segment rising to over 20 per cent for some banks, as against 5-6 per cent during the last financial year, card issuers are more content dealing with people who pay their bills on time.<br>
        <br>
        For instance, in case of SBI Cards, NPAs were estimated to 21.22 per cent as on September 30, 2008, according to rating agency Icra. To tackle this, the company has opted for foreclosures and settlements, and its card base shrunk from 3.4 million to 3 million in the last six months. When contacted, SBI Cards refused to comment.<br>
        <br>
        Among other measures, the basic qualifying parameters have been tightened. The threshold salary limit has been hiked from Rs 8,000-10,000 to Rs 20,000-30,000. Bankers said that they were more comfortable with customers with a higher salary base.<br>
