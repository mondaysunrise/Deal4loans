      <div> It is not just the number of credit cards in use that is falling, but even spending through this route is dropping.<br>
        <br>
        Credit controls put in place by card companies in the wake of rising delinquency levels have also meant that spending through credit cards fell by 7.85 per cent to Rs 5,171.06 crore in January 2009, as against Rs 5,611.38 crore during April 2008. In contrast, spending had gone up by 28 per cent during the corresponding period last year.<br>
        <br>
        According to RBI data released earlier, the combined credit card outstanding went up by almost 70 per cent in the last one year. The outstandings rose to Rs 29,359 crore at the end of December 2008 from Rs 17,306 crore in the corresponding month of 2007. The numbers indicated that many card holders were rolling over credit.<br>
        <br>
        “It may be partly due to the fall in card population, but the average spend is also coming down,” the retail banking head of a large private bank said.<br>
